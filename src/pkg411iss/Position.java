package pkg411iss;

/**
 *
 * @author Jack
 */
public class Position {
    
    private String lat;
    private String lon;

    public Position(String lat, String lon) {
        this.lat = lat;
        this.lon = lon;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLon() {
        return lon;
    }

    public void setLon(String lon) {
        this.lon = lon;
    }
    
    
    
}
